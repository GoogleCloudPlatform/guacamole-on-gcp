# !/bin/bash

FILE=./client/db-secrets.properties

if [ -z ${1} ]; then
    echo "Usage: bin/create-db-secrets.sh [CLOUD_SQL_USER] [CLOUD_SQL_PASSWORD]"
    exit 1
else
    if [ -z ${2} ]; then
        echo "Enter Cloud SQL User Password:"
        read MYSQL_PASSWORD
    else
        MYSQL_PASSWORD=${2}
    fi

    MYSQL_USER=${1}

    echo "# Auto generated by create-db-secrets.sh" > ${FILE}
    echo "MYSQL_USER=$MYSQL_USER" >> ${FILE}
    echo "MYSQL_PASSWORD=$MYSQL_PASSWORD" >> ${FILE}

    exit 0
fi