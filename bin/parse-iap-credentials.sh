# !/bin/bash

FILE=./client/iap-secrets.properties
JQ=/usr/bin/jq

if [ -z ${1} ]; then
    echo "Usage: bin/parse-iap-credentials.sh /path/to/oauth-credentials.json"
    exit 1
else
    CLIENT_ID=`$JQ -r ".web.client_id" ${1}`
    CLIENT_SECRET=`$JQ -r ".web.client_secret" ${1}`
    echo "# Autogenerated by bin/parse-iap-credentials.sh" > ${FILE}
    echo "client_id=${CLIENT_ID}" >> ${FILE}
    echo "client_secret=${CLIENT_SECRET}" >> ${FILE}

    echo "Created ${FILE}"
    echo "You should now remove ${1}, as it is sensitive and no longer required."
    echo "Treat ${FILE} as sensitive, it contains GCP credentials."
    rm -i ${1}
    exit 0
fi
