# !/bin/bash

FILE=./client/guacamole.properties
GCLOUD=/usr/bin/gcloud

GOOGLE_CLIENT_SERVICE_NAME=guacamole-client-service
GOOGLE_CLIENT_SERVICE_PORT=80
GOOGLE_CLIENT_SERVICE_NAMESPACE=guacamole

if [ -z ${DEVSHELL_PROJECT_ID} ]; then
    echo "DEVSHELL_PROJECT_ID is not set - set manually, or run this script in GCP Cloud Shell"
    exit 1
else
    GOOGLE_PROJECT_ID=$DEVSHELL_PROJECT_ID
    GOOGLE_PROJECT_NUMBER=`gcloud projects describe $DEVSHELL_PROJECT_ID --format "value(projectNumber)"`

    echo "# Auto generated by create-guacamole-properties.sh" > ${FILE}
    echo "google-project-id=$GOOGLE_PROJECT_ID" >> ${FILE}
    echo "google-project-number=$GOOGLE_PROJECT_NUMBER" >> ${FILE}
    echo "google-client-service-name=$GOOGLE_CLIENT_SERVICE_NAME" >> ${FILE}
    echo "google-client-service-port=$GOOGLE_CLIENT_SERVICE_PORT" >> ${FILE}
    echo "google-client-service-namespace=$GOOGLE_CLIENT_SERVICE_NAMESPACE" >> ${FILE}

    exit 0
fi